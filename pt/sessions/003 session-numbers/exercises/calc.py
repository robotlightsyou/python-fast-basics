#! /usr/bin/env python3

"""
This program is a basic calculator providing the user addition,
subtraction, multiplication, float division, integer division,
and the modulo operation.
"""


def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def mult(a, b):
    return a * b


def fdiv(a, b):
    return a / b


def idiv(a, b):
    return a // b


def mod(a, b):
    return a % b

def exp(a, b):
    return a ** b

